public class andornot {
    public static void main(String[] args) {

//This defines the booleans
boolean cat = true;
boolean cattwo = false;

// this is for the and example
System.out.println(cat && cattwo);

//this is for the or example
System.out.println(cat || cattwo);

// this is for the not example
System.out.println(cat != cattwo);

    }
}
